-- Tests which perform one multi-scalar multiplication and check that the result
-- is equal to an expected result.
data Params1 = Params1
  { scalars :: [Integer]
  , points :: [BI.BuiltinByteString]
  , output :: BI.BuiltinByteString
  }
PlutusTx.unstableMakeIsData ''Params1
PlutusTx.makeLift ''Params1

{-# INLINEABLE mkSucceedingMultiScalarMulPolicy1 #-}
mkSucceedingMultiScalarMulPolicy1 :: [Params1] -> P.BuiltinData -> P.BuiltinUnit
mkSucceedingMultiScalarMulPolicy1 l _ctx = go l
 where
  go [] = BI.unitval
  go (Params1{..} : rest) =
    if BI.bls12_381_G2_multiScalarMul scalars (fmap BI.bls12_381_G2_uncompress points)
      `BI.bls12_381_G2_equals` (BI.bls12_381_G2_uncompress output)
      then go rest
      else P.traceError "mkSucceedingMultiScalarMulPolicy"


{-# INLINEABLE mkSucceedingMultiScalarMulPolicy1 #-}
mkSucceedingMultiScalarMulPolicy1 :: [Params1] -> P.BuiltinData -> P.BuiltinUnit
mkSucceedingMultiScalarMulPolicy1 l _ctx = go l
 where
  go [] = BI.unitval
  go (Params1{..} : rest) =
    if BI.bls12_381_G1_multiScalarMul scalars (fmap BI.bls12_381_G1_uncompress points)
      `BI.bls12_381_G1_equals` (BI.bls12_381_G1_uncompress output)
      then go rest
      else P.traceError "mkSucceedingMultiScalarMulPolicy1"

